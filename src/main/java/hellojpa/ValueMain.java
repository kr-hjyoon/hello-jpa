package hellojpa;

public class ValueMain{
    public static void main(String[] args) {

        int a = 1;
        int b = a;

        System.out.println("a="+a );
        System.out.println("b="+b );

        Integer wa = 1;
        Integer wb = a;

        wa = 2;

        System.out.println("wa="+wa );
        System.out.println("wb="+wb );



    }
}
